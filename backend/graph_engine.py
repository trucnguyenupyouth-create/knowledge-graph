def _get_psycopg2_connection():
    """
    Return a live psycopg2 connection using ``DIRECT_DB_URL`` from the
    environment.  Raises ``RuntimeError`` if the variable is absent.

    The caller is responsible for closing the connection (use as a context
    manager or call ``.close()`` explicitly).
    """
    try:
        import psycopg2  # type: ignore
        import psycopg2.extras  # type: ignore
    except ImportError as exc:
        raise RuntimeError(
            "psycopg2 is not installed.  Run: pip install psycopg2-binary"
        ) from exc

    db_url = os.environ.get("DIRECT_DB_URL")
    if not db_url:
        raise RuntimeError(
            "DIRECT_DB_URL environment variable is not set. "
            "Set it to your Supabase PostgreSQL connection string, e.g.: "
            "postgresql://postgres:<password>@db.<project>.supabase.co:5432/postgres"
        )
    return psycopg2.connect(db_url, cursor_factory=psycopg2.extras.RealDictCursor)


def _get_supabase_client():
    """
    Return a configured ``supabase-py`` client using SUPABASE_URL and
    SUPABASE_SERVICE_ROLE_KEY from the environment.

    We use the *service role* key (not the anon key) so that the backend can
    bypass Row-Level-Security and read all data.
    """
    try:
        from supabase import create_client, Client  # type: ignore
    except ImportError as exc:
        raise RuntimeError(
            "supabase-py is not installed.  Run: pip install supabase"
        ) from exc

    url = os.environ.get("SUPABASE_URL")
    key = os.environ.get("SUPABASE_SERVICE_ROLE_KEY")
    if not url or not key:
        raise RuntimeError(
            "SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY must both be set "
            "in the environment."
        )
    return create_client(url, key)


