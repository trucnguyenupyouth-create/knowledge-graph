def _execute_cte_direct(sql: str, params: dict) -> List[dict]:
    """
    Execute a parameterised SQL query via a direct psycopg2 connection and
    return all rows as a list of plain dicts.

    Parameters
    ----------
    sql:    The SQL string with ``%(name)s`` placeholders.
    params: Dict mapping placeholder names to values.

    Returns
    -------
    List of row dicts; empty list if no rows matched.
    """
    conn = _get_psycopg2_connection()
    try:
        with conn.cursor() as cur:
            cur.execute(sql, params)
            rows = cur.fetchall()
            # psycopg2 RealDictCursor returns RealDictRow objects; cast to plain
            # dict so Pydantic model_validate() doesn't need psycopg2 installed.
            return [dict(row) for row in rows]
    finally:
        conn.close()


def _execute_cte_via_rpc(rpc_function: str, params: dict) -> List[dict]:
    """
    Execute a graph traversal by calling a pre-registered PostgreSQL function
    via Supabase rpc().

    The PostgreSQL function must be created in your Supabase project before
    this path is used.  See ``backend/database/setup_rpc.sql`` for the DDL.

    Parameters
    ----------
    rpc_function: Name of the PostgreSQL function to call.
    params:       Dict of named parameters passed as the JSON argument to rpc().

    Returns
    -------
    List of row dicts returned by the function.
    """
    client = _get_supabase_client()
    response = client.rpc(rpc_function, params).execute()
    # supabase-py >= 2.x raises on HTTP errors; `.data` holds the result list.
    return response.data or []


def _execute_sqlite_direct(sql: str, params: dict) -> List[dict]:
    """Execute SQL via local SQLite DB when Supabase config is absent."""
    import sqlite3
    db_path = os.environ.get("SQLITE_DB_PATH", "math_graph.db")
    if not os.path.exists(db_path):
        raise RuntimeError(f"SQLite database not found at {db_path}")
    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    try:
        # Convert psycopg2 placeholder style %(param)s to SQLite style :param
        for k in params.keys():
            sql = sql.replace(f"%({k})s", f":{k}")
            
        cur = conn.cursor()
        cur.execute(sql, params)
        rows = cur.fetchall()
        return [dict(row) for row in rows]
    finally:
        conn.close()


def _run_query(sql: str, params: dict, rpc_fallback: str) -> List[dict]:
    """
    Dispatch a graph query to the correct backend.

    Priority:
      1. ``DIRECT_DB_URL`` is set  →  raw psycopg2.
      2. ``SUPABASE_URL`` is set   →  supabase-py rpc().
      3. Fallback                  →  local SQLite DB.
    """
    if os.environ.get("DIRECT_DB_URL"):
        logger.debug("Graph engine: using direct psycopg2 connection.")
        return _execute_cte_direct(sql, params)
    elif os.environ.get("SUPABASE_URL"):
        logger.debug("Graph engine: using Supabase rpc() → %s.", rpc_fallback)
        rpc_params = {f"p_{k}": v for k, v in params.items()}
        return _execute_cte_rpc(rpc_fallback, rpc_params)
    else:
        logger.debug("Graph engine: using local SQLite connection.")
        return _execute_sqlite_direct(sql, params)


# Alias to avoid shadowing the public name used below.
_execute_cte_rpc = _execute_cte_via_rpc


