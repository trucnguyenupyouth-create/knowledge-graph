def _verify_node_exists(concept_code: str) -> None:
    """
    Raise ``LookupError`` if the given concept_code is not present in the DB.

    This is a lightweight existence check (SELECT 1 LIMIT 1) to give callers
    a clean LookupError instead of an empty/ambiguous result set.

    Parameters
    ----------
    concept_code: Already-normalised (stripped, uppercased) concept code.

    Raises
    ------
    LookupError: If no node with this concept_code exists.
    RuntimeError: On database failure.
    """
    existence_sql = """
        SELECT 1 AS node_exists
        FROM   nodes
        WHERE  concept_code = %(concept_code)s
        LIMIT  1;
    """

    if os.environ.get("DIRECT_DB_URL"):
        rows = _execute_cte_direct(existence_sql, {"concept_code": concept_code})
    elif os.environ.get("SUPABASE_URL"):
        # Via supabase-py PostgREST (no raw SQL needed for a simple equality filter).
        client = _get_supabase_client()
        resp = (
            client.table("nodes")
            .select("concept_code")
            .eq("concept_code", concept_code)
            .limit(1)
            .execute()
        )
        rows = resp.data or []
    else:
        # SQLite fallback
        rows = _execute_sqlite_direct(existence_sql, {"concept_code": concept_code})

    if not rows:
        raise LookupError(
            f"Concept code '{concept_code}' was not found in the nodes table. "
            "Verify the code is correct and has been seeded into the database."
        )
