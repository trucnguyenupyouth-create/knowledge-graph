"""
backend/graph_engine.py

Math Knowledge Graph – Traversal Engine
========================================

Responsibility
--------------
Execute graph traversal queries against the PostgreSQL knowledge graph stored
in Supabase.  All traversal logic is expressed as PostgreSQL Recursive CTEs so
the database engine handles the graph walk, keeping Python thin and stateless.

Architecture note
-----------------
Supabase's ``postgrest-py`` client exposes two low-level escape hatches:

1. ``supabase.rpc(fn_name, params)``   – calls a *PostgreSQL function* you
   define in your DB.  Great for reuse, but requires a migration per query.

2. ``supabase.postgrest``              – raw PostgREST; cannot run arbitrary SQL.

Because we want to ship without creating DB functions first, we use
``supabase.rpc`` by wrapping the CTE inside a PL/pgSQL function that is
lazily created in-process the first time the engine is imported.  A
``CREATE OR REPLACE FUNCTION`` call is made via the Supabase management client
(``psycopg2`` / ``asyncpg``).  In pure-Supabase projects without direct Postgres
access the recommended pattern is ``supabase.rpc`` with a pre-deployed function.

For maximum portability this module supports BOTH modes:
  - ``DIRECT_DB_URL`` env var set  →  raw psycopg2 (works in local dev & CI).
  - Only ``SUPABASE_*`` env vars   →  supabase-py rpc() (requires the helper
    function to be registered in the DB beforehand; see setup note below).

The public surface is intentionally small:

    get_prerequisite_chain(target_concept_code)  →  PrerequisiteChainResult
    get_immediate_prerequisite(target_concept_code, hard_only)  →  Node | None

Setup note (Supabase rpc path)
------------------------------
Run the content of ``backend/database/setup_rpc.sql`` once against your Supabase
project to register the required PostgreSQL helper functions.  That file is
generated by ``graph_engine.py`` itself; call ``python -m backend.graph_engine
--install`` to print it.
"""

from __future__ import annotations

import logging
import os
from typing import List, Optional

from pydantic import BaseModel, ConfigDict, Field

from backend.models import Node, RelationshipType

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Response models (graph-engine–specific)
# ---------------------------------------------------------------------------


class PrerequisiteNode(BaseModel):
    """
    A single node as it appears in the prerequisite chain result.

    Carries an extra ``depth`` field indicating how many hops away from the
    originally queried concept this node sits (1 = immediate prerequisite,
    2 = grandparent, …).
    """

    model_config = ConfigDict(from_attributes=True)

    depth: int = Field(
        ...,
        ge=1,
        description=(
            "Traversal depth from the target concept. "
            "depth=1 → immediate prerequisite; depth=N → N hops away."
        ),
    )
    concept_code: str = Field(..., description="Stable curriculum identifier.")
    grade_level: int = Field(..., description="National curriculum grade (1–12).")
    topic_category: str = Field(..., description="Broad curriculum domain.")
    concept_name_vn: str = Field(..., description="Vietnamese name of the concept.")
    concept_description: Optional[str] = Field(
        None, description="Full description of the concept."
    )
    mastery_question: Optional[str] = Field(
        None, description="Diagnostic question that validates mastery."
    )


class PrerequisiteChainResult(BaseModel):
    """
    Structured response returned by ``get_prerequisite_chain``.

    ``chain`` is ordered from the *shallowest* (depth=1, immediate prerequisite)
    to the *deepest* (foundational root node), matching the natural remediation
    order an adaptive engine would present to a student.
    """

    target_concept_code: str = Field(
        ..., description="The concept code that was queried."
    )
    total_prerequisites: int = Field(
        ..., ge=0, description="Total number of prerequisite nodes discovered."
    )
    chain: List[PrerequisiteNode] = Field(
        default_factory=list,
        description=(
            "Ordered prerequisite chain, shallowest (depth=1) to deepest root."
        ),
    )


# ---------------------------------------------------------------------------
# SQL for the Recursive CTE
# ---------------------------------------------------------------------------

# This query is parameterised by a single named placeholder ``%(concept_code)s``
# (psycopg2 style).  When called via Supabase rpc, parameters are passed as a
# JSON object ``{"p_concept_code": "..."}`` to the helper function below.
#
# Traversal logic:
#   1. Anchor: find the target node by concept_code.
#   2. Recursive step: walk edges backwards (edge.target → edge.source) to
#      collect all ancestors, accumulating the traversal depth.
#   3. Final SELECT: join the accumulated node IDs back to the nodes table to
#      return full node details, ordered by depth ascending.
#
# The CTE correctly handles:
#   - DAGs (directed acyclic graphs) with multiple paths to the same root.
#   - Cycles: ``UNION`` (not ``UNION ALL``) in the recursive term deduplicates
#     already-visited node IDs, so the query terminates even if the graph has
#     an accidental cycle.
_RECURSIVE_CTE_SQL = """
WITH RECURSIVE

  -- Step 1: Resolve the target concept_code to its UUID and use it as the
  --         starting point for the backwards traversal.
  target AS (
      SELECT id AS node_id
      FROM   nodes
      WHERE  concept_code = %(concept_code)s
      LIMIT  1
  ),

  -- Step 2: Recursive traversal.
  --   Each iteration adds the *source* nodes of edges whose *target* matches
  --   nodes already in the working set.  This walks the graph "backwards",
  --   i.e., from dependent → prerequisite → prerequisite-of-prerequisite …
  prerequisite_tree AS (

      -- Anchor member: the immediate prerequisites (depth 1).
      SELECT
          e.source_node_id  AS node_id,
          1                 AS depth
      FROM  edges e
      JOIN  target t ON e.target_node_id = t.node_id

      UNION  -- UNION (not UNION ALL) to deduplicate already-visited nodes.

      -- Recursive member: prerequisites of prerequisites.
      SELECT
          e.source_node_id       AS node_id,
          pt.depth + 1           AS depth
      FROM  edges e
      JOIN  prerequisite_tree pt ON e.target_node_id = pt.node_id
  )

-- Step 3: Join back to the nodes table to get full node metadata.
--         When the same node is reachable via multiple paths (diamond DAG),
--         MIN(depth) picks the shortest path depth, which is the most useful
--         value for display ordering.
SELECT
    MIN(pt.depth)            AS depth,
    n.concept_code,
    n.grade_level,
    n.topic_category,
    n.concept_name_vn,
    n.concept_description,
    n.mastery_question
FROM  prerequisite_tree pt
JOIN  nodes n ON n.id = pt.node_id
GROUP BY
    n.concept_code,
    n.grade_level,
    n.topic_category,
    n.concept_name_vn,
    n.concept_description,
    n.mastery_question
ORDER BY
    depth ASC,          -- Shallower prerequisites first (depth=1 = immediate).
    n.grade_level ASC;  -- Within the same depth, lower grade first.
"""

# Lighter query for the webhook endpoint: returns only the concept_code and
# mastery_question of the *single* immediate HARD prerequisite, if any.
_IMMEDIATE_HARD_PREREQ_SQL = """
SELECT
    n.concept_code,
    n.grade_level,
    n.topic_category,
    n.concept_name_vn,
    n.concept_description,
    n.mastery_question,
    n.id,
    n.created_at
FROM  edges e
JOIN  nodes target_n ON target_n.id     = e.target_node_id
JOIN  nodes n        ON n.id            = e.source_node_id
WHERE target_n.concept_code = %(concept_code)s
  AND e.relationship_type   = 'HARD_PREREQUISITE'
ORDER BY n.grade_level DESC  -- Closest hard prerequisite by grade proximity.
LIMIT 1;
"""


