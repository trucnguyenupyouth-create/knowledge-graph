def get_prerequisite_chain(target_concept_code: str) -> PrerequisiteChainResult:
    """
    Return the full prerequisite chain for a given concept, traversing from
    the immediate prerequisites all the way down to the foundational root nodes.

    Traversal is performed by a single PostgreSQL Recursive CTE, so the entire
    graph walk happens inside the database with no round-trips per hop.

    Parameters
    ----------
    target_concept_code:
        The ``concept_code`` of the concept whose prerequisites we want.
        Example: ``"G9_ALG_QUAD_EQ"`` (Quadratic Equations, Grade 9).

    Returns
    -------
    PrerequisiteChainResult
        ``.chain`` is ordered shallowest → deepest (remediation order).
        ``.total_prerequisites`` is the count of unique ancestors found.
        If the concept has no prerequisites, ``.chain`` is an empty list.

    Raises
    ------
    ValueError
        If ``target_concept_code`` is empty or blank.
    LookupError
        If the concept_code does not exist in the ``nodes`` table.
    RuntimeError
        On database connectivity or configuration failures.
    """
    # --- Input sanitisation --------------------------------------------------
    concept_code = target_concept_code.strip().upper()
    if not concept_code:
        raise ValueError("target_concept_code must be a non-empty string.")

    logger.info("Graph traversal requested for concept_code='%s'.", concept_code)

    # --- Verify the target node exists (fast single-row lookup) --------------
    # We do this before the expensive CTE so we can return a clean LookupError
    # rather than an empty list that could be misread as "no prerequisites".
    _verify_node_exists(concept_code)

    # --- Execute the Recursive CTE -------------------------------------------
    try:
        rows = _run_query(
            sql=_RECURSIVE_CTE_SQL,
            params={"concept_code": concept_code},
            rpc_fallback="get_prerequisite_chain",  # matches setup_rpc.sql
        )
    except Exception as exc:
        logger.exception(
            "Database error during prerequisite traversal for '%s'.", concept_code
        )
        raise RuntimeError(
            f"Failed to execute prerequisite traversal for '{concept_code}': {exc}"
        ) from exc

    # --- Parse rows into typed models ----------------------------------------
    chain: List[PrerequisiteNode] = []
    for row in rows:
        try:
            chain.append(PrerequisiteNode.model_validate(row))
        except Exception as parse_err:
            # Log malformed rows but don't abort the entire response.
            logger.warning(
                "Could not parse prerequisite row for '%s': %s | row=%s",
                concept_code,
                parse_err,
                row,
            )

    logger.info(
        "Prerequisite chain for '%s': %d nodes found (deepest depth=%d).",
        concept_code,
        len(chain),
        chain[-1].depth if chain else 0,
    )

    return PrerequisiteChainResult(
        target_concept_code=concept_code,
        total_prerequisites=len(chain),
        chain=chain,
    )


def get_immediate_prerequisite(
    target_concept_code: str,
    hard_only: bool = True,
) -> Optional[Node]:
    """
    Return the *single* most relevant immediate prerequisite for the given
    concept, or ``None`` if none exists.

    This is the hot path for the AI grading webhook: when a student fails a
    concept, the webhook calls this function to surface the exact mastery
    question the student needs to answer next.

    Parameters
    ----------
    target_concept_code:
        The concept_code of the *failed* concept.
    hard_only:
        If ``True`` (default), only HARD_PREREQUISITEs are considered.
        Set to ``False`` to also include SOFT_PREREQUISITEs.

    Returns
    -------
    ``Node`` for the most proximate prerequisite, or ``None`` if not found.

    Raises
    ------
    LookupError
        If the concept_code does not exist in the ``nodes`` table.
    RuntimeError
        On database connectivity or configuration failures.
    """
    concept_code = target_concept_code.strip().upper()
    if not concept_code:
        raise ValueError("target_concept_code must be a non-empty string.")

    # Verify the concept exists before we run a more expensive query.
    _verify_node_exists(concept_code)

    # Build SQL dynamically based on hard_only flag.
    rel_filter = (
        "AND e.relationship_type = 'HARD_PREREQUISITE'"
        if hard_only
        else ""
    )
    sql = f"""
        SELECT
            n.id,
            n.concept_code,
            n.grade_level,
            n.topic_category,
            n.concept_name_vn,
            n.concept_description,
            n.mastery_question,
            n.created_at
        FROM  edges e
        JOIN  nodes target_n ON target_n.id  = e.target_node_id
        JOIN  nodes n        ON n.id         = e.source_node_id
        WHERE target_n.concept_code = %(concept_code)s
          {rel_filter}
        ORDER BY n.grade_level DESC
        LIMIT 1;
    """

    try:
        rows = _run_query(
            sql=sql,
            params={"concept_code": concept_code},
            rpc_fallback="get_immediate_prerequisite",
        )
    except Exception as exc:
        logger.exception(
            "Database error fetching immediate prerequisite for '%s'.", concept_code
        )
        raise RuntimeError(
            f"Failed to fetch immediate prerequisite for '{concept_code}': {exc}"
        ) from exc

    if not rows:
        logger.info(
            "No immediate prerequisite found for '%s' (hard_only=%s).",
            concept_code,
            hard_only,
        )
        return None

    return Node.model_validate(rows[0])


